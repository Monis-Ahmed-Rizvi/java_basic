// wrapper class for primitive data type
package t_jav;

import java.util.*;
import java.lang.*;


public class wrap_class {

	
	public static void main(String[] args)
	{
		
	    String num = "12345";
	    // Integer is a warper class for int  
	    int num1  = Integer.parseInt(num);
	    
	    System.out.println(num1);
	    
	    
	    String str = Integer.toString(num1);
	    
	    System.out.println(str);
	    
	    
	    
	    
	    
		
		
	}
	
}
