// a program to to check whether a given number odd or even

package quick_basic;
import java.util.* ;



public class ternary {
     public static void main(String args[])
     {
    	 
    	 Scanner sc = new Scanner(System.in);
    	 System.out.print("enter number");
    	 String  result;
    	 int x1 = sc.nextInt();
    	 
    	 result = (x1 % 2 == 0) ? "even" : "odd";  
    	 
    	 System.out.println(result);
    	 
    	 
    	 
    	 
    	 
    	 
    	 
     }
}
